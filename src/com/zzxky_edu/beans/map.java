package com.zzxky_edu.beans;

public class map {
	// 导航id
	private Integer map_id;
	//导航内容
	private String map_content;
	
	//导航链接
	private String map_links;
	

	public Integer getMap_id() {
		return map_id;
	}

	public void setMap_id(Integer map_id) {
		this.map_id = map_id;
	}

	public String getMap_content() {
		return map_content;
	}

	public void setMap_content(String map_content) {
		this.map_content = map_content;

	}

	public String getMap_links() {
		return map_links;
	}

	public void setMap_links(String map_links) {
		this.map_links = map_links;
	}

}
